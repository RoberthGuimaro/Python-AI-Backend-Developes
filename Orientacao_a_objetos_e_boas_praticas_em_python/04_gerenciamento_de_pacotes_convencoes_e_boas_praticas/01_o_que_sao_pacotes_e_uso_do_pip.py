"""
    O que são pacotes em Python?
    - Pacotes são módulos que podem ser instalados e utilizados em
    seus programas Python. Eles permitem que você utilize código que
    foi escrito por outras pessoas, economizando tempo e esforço.

    O papel do PIP
    - Pip  é o gerenciador de pacotes do Python. Ele nos permite
    instalar, atualziar e remover pacotes facilmente. Ele se
    comunica com o PyPI (Python Package Index), que é onde a
    maioria dos pacotes Python são armazenados.

    Uso de ambientes virtuais
    - Ambientes virtuais, como os criados por venvs, nos permitem
    manter as dependências de diferentes projetos. Isso é importante
    para evitar conflitos entre versões de pacotes.

        Exemplo de código:
        python3 -m venv myenv
        source myenv/bin/activate
"""