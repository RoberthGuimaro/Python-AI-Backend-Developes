"""
    O que é o MongoDB?
    - Banco de dados NoSQL orientado a documentos.
    - Grandes volumes de dados, escalabilidade horizontal e modelagem
    flexível.
    - Não exige um esquema.
    - Permite que os documentos sejam armazenados em formato BSON 
    (Binary JSON), proporcionando uma estrutura semiestruturada.

    Vatangens
    - Flexibilidade na modelagem de dados.
    - Escalabilidade horizontal para lidar com grandes volumes de dados.
    - Consultas ricas e suporte a consultas complexas.
    - Alta disponibilidade e tolerância a falhas.
    - Comunidade ativa e recursos de suporte.

    Desvantagens
    - Menor consistência imediata em comparação com bancos de dados
    relacionais.
    - Consultas complexas podem exigir um maior conhecimento e planejamento
    adequado.
    - Maior consumo de espaço de armazenamento em comparação com bancos de
    dados relacionais devido à flexibiliade dos documentos.

    Onde o MongoDB é usado
    - Aplicações web: onde a flexibilidade e a escalabilidade são cruciais
    para lidar com olumes variáveis de dados.

    - Analise de big data: análise de grandes volumes de dados não estruturados
    ou semiestruturados, fornecendo uma plataforma para armzenar e processar
    esses dados.

    - Armazenamento de dados semiestruturados: permite a inserção de documentos
    com estruturas diferentes em uma mesma coleção.

    - Casos de uso de geolocalização: Com suas funcionalidades de consulta
    geoespacial, é adequado par acasos de uso que envolvem dados baseados
    em localização, como aplicativos de mapeamento e rastreamento.
    
"""